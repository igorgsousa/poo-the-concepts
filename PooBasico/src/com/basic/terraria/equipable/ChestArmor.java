package com.basic.terraria.equipable;

public class ChestArmor {
	
	private int armor;
	private String name;
	
	public int getArmor() {
		return armor;
	}
	public void setArmor(int armor) {
		this.armor = armor;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
